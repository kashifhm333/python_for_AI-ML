# here are the all keywords in python with comments:
# keywords are reserved words in python that have a special meaning and cannot be used as
# identifiers (variable names, function names, etc.). They are used to define the syntax and 
# structure of the python language. Some common keywords in python include:

# 1. if - used for conditional statements
# 2. else - used for conditional statements
# 3. elif - used for conditional statements
# 4. for - used for loops
# 5. while - used for loops
# 6. def - used to define functions
# 7. return - used to return a value from a function
# 8. import - used to import modules
# 9. from - used to import specific functions or variables from a module
# 10. as - used to create an alias for a module or function
# 11. class - used to define a class    
# 12. try - used for exception handling
# 13. except - used for exception handling
# 14. finally - used for exception handling
# 15. with - used for context management
# 16. pass - used as a placeholder for code that is not yet implemented
# 17. break - used to exit a loop
# 18. continue - used to skip the current iteration of a loop
# 19. global - used to declare a global variable
# 20. nonlocal - used to declare a nonlocal variable
# 21. lambda - used to create anonymous functions
# 22. yield - used to create generator functions
# 23. assert - used for debugging purposes
# 24. del - used to delete a variable or an object
# 25. raise - used to raise an exception
# 26. in - used to check if a value is present in a sequence (like  a list, tuple, etc.)
# 27. is - used to check if two variables refer to the same object in memory
# 28. not - used to negate a boolean expression
# 29. and - used to combine boolean expressions
# 30. or - used to combine boolean expressions  


