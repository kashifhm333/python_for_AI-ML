# variable are containers for storing data values. In python there are different types of variables 
# such as string, integer, float, boolean etc. We can assign values to variables and use them in our program

name= "Kashif Hussain" # string variable
age= 25 # integer variable
height= 5.8 # float variable
is_student= True # boolean variable

print("My name is:", name)
print("My age is:", age)
print("My age is:", age-5)   # we can perform operations on variables
print("My height is:", height)
print("Am I a student?", is_student)

# all values at ones
print("My name is:", name, "My age is:", age, "My height is:", height, "Am I a student?", is_student)



