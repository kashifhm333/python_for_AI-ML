age = 43


print("my name is {} and i am {} years old".format("kashif", age))


# Normal Formatting 
print("i am {} years old".format(age))

# indexing
print("i am {0} and i am {1} years old".format("kashif",38))



# value based formatting
print("i am {name} and i am {age} years old".format(name="kashif", age=38))


# f-string formatting
print(f"i am {age} years old")


