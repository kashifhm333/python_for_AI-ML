# data_types in python are used to define the type of data that a variable can hold. Some common data types in python include:
# 1. Integer (int) - represents whole numbers
# 2. Float (float) - represents decimal numbers
# 3. String (str) - represents a sequence of characters
# 4. Boolean (bool) - represents either True or False
# 5. None - represents the absence of a value.

# we can use the type() function to check the data type of a variable
# example of data types in python

x = 10 # integer
y = 3.14 # float

name = "Kashif Hussain" # string

is_student = True # boolean

none_value = None # none

print("The data type of", x, "is:", type(x))

print("The data type of", y, "is:", type(y))

print("The data type of", name, "is:", type(name))

print("The data type of", is_student, "is:", type(is_student))

print("The data type of", none_value, "is:", type(none_value))

